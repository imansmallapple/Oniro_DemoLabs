/**
 * Multimedia data model
 */
export class MediaBean {
  /**
   * File name
   */
  public fileName: string;

  /**
   * File size
   */
  public fileSize: number;

  /**
   * File type (e.g., jpg, png, mp4)
   */
  public fileType: string;

  /**
   * Local storage path
   */
  public localUrl: string;
}
